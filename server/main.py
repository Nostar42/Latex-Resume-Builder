# Global AI performance metrics — stored per model.
# Persisted to temp/metrics.json so they survive server restarts.
#
# In-memory structure:  _metrics: dict[model_name, list[entry]]
# On-disk structure:    {"qwen2.5-coder:3b": [...], "phi4": [...], ...}
#
# Each entry records (see _record_metric for full field list):
#   ts              Unix timestamp of the request (seconds)
#   model           Ollama model name used
#   mode            Interaction mode: "resume", "math", or "image"
#   elapsed_ms      Total wall-clock time for the full request (AI + compile)
#   prompt_tokens   Token count of the prompt sent to the model
#   gen_tokens      Token count generated by the model in the response
#   tokens_per_sec  Model generation speed (gen_tokens / AI time)
#   edit_ok         Whether the SEARCH/REPLACE edit was applied (resume mode)
#   compile_ok      Whether pdflatex compiled without error (null = not attempted)
#
# Cap: _METRICS_CAP entries per model (oldest dropped when exceeded).
# ---------------------------------------------------------------------------
_METRICS_CAP  = 1000                          # max entries stored per model
_metrics: dict[str, list[dict]] = {}          # keyed by model name
_metrics_lock = threading.Lock()              # guards both the dict and file writes


def _load_metrics() -> dict[str, list[dict]]:
    """Read persisted metrics from disk at startup.

    Handles two formats:
    - New  (dict)  — {model: [entries, ...]}  written by this version.
    - Legacy (list) — flat list written by earlier versions; auto-migrated
      by bucketing each entry on its "model" field.

    Returns {} on any error.
    """
    try:
        if METRICS_FILE.exists():
            data = json.loads(METRICS_FILE.read_text(encoding="utf-8"))
            if isinstance(data, dict):
                # New per-model format — enforce cap on each bucket.
                return {
                    model: bucket[-_METRICS_CAP:]
                    for model, bucket in data.items()
                    if isinstance(bucket, list)
                }
            elif isinstance(data, list):
                # Legacy flat list — migrate by grouping on model name.
                print("  INFO metrics: migrating legacy flat list to per-model format.")
                bucketed: dict[str, list[dict]] = {}
                for entry in data:
                    m = entry.get("model", "unknown") if isinstance(entry, dict) else "unknown"
                    bucketed.setdefault(m, []).append(entry)
                return {
                    model: bucket[-_METRICS_CAP:]
                    for model, bucket in bucketed.items()
                }
    except Exception as exc:
        print(f"  WARN metrics load: {exc}")
    return {}


def _save_metrics_locked() -> None:
    """Write _metrics to disk atomically. Caller must hold _metrics_lock.

    Writes to a .tmp file first, then renames over the real file so a
    crash or Ctrl+C mid-write never leaves a corrupted metrics.json.
    """
    try:
        tmp = METRICS_FILE.with_suffix(".tmp")
        tmp.write_text(
            json.dumps(_metrics, separators=(",", ":")),
            encoding="utf-8",
        )
        os.replace(tmp, METRICS_FILE)   # atomic on POSIX; near-atomic on Windows
    except Exception as exc:
        print(f"  WARN metrics save: {exc}")


def _record_metric(entry: dict) -> None:
    """Append entry to the per-model bucket, enforcing the per-model cap."""
    model = entry.get("model", "unknown")
    with _metrics_lock:
        bucket = _metrics.setdefault(model, [])
        bucket.append(entry)
        if len(bucket) > _METRICS_CAP:
            _metrics[model] = bucket[-_METRICS_CAP:]
        _save_metrics_locked()

