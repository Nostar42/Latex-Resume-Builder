@app.get("/api/metrics")
async def get_metrics(request: Request):
    """Return per-model AI performance metrics + cross-model aggregate summary.

    Response shape
    ──────────────
    {
      "entries":  [ ...50 most-recent entries across all models, newest last ],
      "summary":  {
        "total":              int,    # total requests recorded across all models
        "model_count":        int,    # number of distinct models seen
        "compile_rate":       float|null,  # % of compile-attempted requests that succeeded
        "avg_elapsed_ms":     int,    # mean wall-clock time across all models
        "avg_tokens_per_sec": float,  # mean generation speed across all models
        "total_tokens":       int,    # total tokens generated across all models
        "by_model": [                 # sorted by request count descending
          {
            "model":              str,
            "count":              int,
            "stored":             int,   # entries kept on disk (≤ 1000)
            "compile_rate":       float|null,
            "avg_elapsed_ms":     int,
            "avg_tokens_per_sec": float,
            "total_tokens":       int,
            "by_mode": {              # breakdown of request count per mode
              "resume": int, "math": int, "image": int
            },
          }, ...
        ],
      },
    }
    """
    if not await _rate_limit(request, _RL_API): return _too_many()

    with _metrics_lock:
        # Snapshot each per-model bucket to avoid holding the lock during aggregation.
        snapshot: dict[str, list[dict]] = {m: list(b) for m, b in _metrics.items()}

    # Flatten to a single sorted list for cross-model aggregates and recent-entries view.
    all_entries: list[dict] = sorted(
        (e for bucket in snapshot.values() for e in bucket),
        key=lambda e: e.get("ts", 0),
    )

    if not all_entries:
        return JSONResponse({"entries": [], "summary": {}})

    # ── Cross-model aggregate ──────────────────────────────────────────────
    total = len(all_entries)

    attempted    = [e for e in all_entries if e.get("compile_ok") is not None]
    compiled     = [e for e in attempted   if e["compile_ok"] is True]
    compile_rate = round(len(compiled) / len(attempted) * 100, 1) if attempted else None

    elapsed_vals = [e["elapsed_ms"]    for e in all_entries if e.get("elapsed_ms")]
    tok_s_vals   = [e["tokens_per_sec"] for e in all_entries if e.get("tokens_per_sec")]
    avg_elapsed_ms = round(sum(elapsed_vals) / len(elapsed_vals)) if elapsed_vals else 0
    avg_tok_s      = round(sum(tok_s_vals)   / len(tok_s_vals),   1) if tok_s_vals else 0
    total_tokens   = sum(e.get("gen_tokens", 0) for e in all_entries)

    # ── Per-model breakdown ────────────────────────────────────────────────
    by_model = []
    for model_name, bucket in sorted(snapshot.items(),
                                     key=lambda kv: len(kv[1]), reverse=True):
        m_attempted = [e for e in bucket if e.get("compile_ok") is not None]
        m_compiled  = [e for e in m_attempted if e["compile_ok"] is True]
        m_elapsed   = [e["elapsed_ms"]     for e in bucket if e.get("elapsed_ms")]
        m_tok_s     = [e["tokens_per_sec"] for e in bucket if e.get("tokens_per_sec")]
        m_modes: dict[str, int] = {"resume": 0, "math": 0, "image": 0}
        for e in bucket:
            md = e.get("mode", "resume")
            if md in m_modes:
                m_modes[md] += 1

        by_model.append({
            "model":              model_name,
            "count":              len(bucket),
            "stored":             len(bucket),   # same as count; explicit for clarity
            "compile_rate":       (round(len(m_compiled) / len(m_attempted) * 100, 1)
                                   if m_attempted else None),
            "avg_elapsed_ms":     round(sum(m_elapsed) / len(m_elapsed)) if m_elapsed else 0,
            "avg_tokens_per_sec": round(sum(m_tok_s)   / len(m_tok_s),   1) if m_tok_s else 0,
            "total_tokens":       sum(e.get("gen_tokens", 0) for e in bucket),
            "by_mode":            m_modes,
        })

    summary = {
        "total":              total,
        "model_count":        len(snapshot),
        "compile_rate":       compile_rate,
        "avg_elapsed_ms":     avg_elapsed_ms,
        "avg_tokens_per_sec": avg_tok_s,
        "total_tokens":       total_tokens,
        "by_model":           by_model,
    }
    # Return the 50 most-recent entries globally (already sorted by ts ascending).
    return JSONResponse({"entries": all_entries[-50:], "summary": summary})


@app.post("/api/chat")
async def chat(request: Request, session_id: Optional[str] = Cookie(None)):
    if not await _rate_limit(request, _RL_CHAT): return _too_many()
    sid, is_new = _resolve(session_id)
    _init_session(sid)

    t0: float = time.monotonic()
    llm_perf: dict | None = None

    try:
        body = await request.json()
    except Exception:
        return _resp({"message": "", "updated": False,
                      "error": "Invalid JSON body."}, sid, is_new, status=400)
    raw_messages = body.get("messages", [])
    if not isinstance(raw_messages, list):
        return _resp({"message": "", "updated": False,
                      "error": "messages must be a list."}, sid, is_new, status=400)
    messages = raw_messages[-20:]
    images   = body.get("images") or []
    if not isinstance(images, list):
        images = []
    mode = body.get("mode", "resume")
    if mode not in ("resume", "math", "image"):
        mode = "resume"                          # unknown modes fall back to edit

    # Guard against oversized image payloads
    total_img_bytes = sum(len(b) for b in images)
    if total_img_bytes > MAX_IMAGE_BYTES:
        return _resp(
            {"message": "", "updated": False,
             "error": f"Image too large ({total_img_bytes // 1_000_000} MB). Max ~6 MB."},
            sid, is_new, status=400,
        )

    tex      = _tex(sid)
    document = tex.read_text(encoding="utf-8") if tex.exists() else ""

    # ── inline helper: record metric + build response ─────────────────────
    def _finish(data: dict, *, edit_ok: bool, compile_ok: bool | None) -> JSONResponse:
        elapsed_ms = round((time.monotonic() - t0) * 1000)
        entry: dict = {
            "ts":            round(time.time()),
            "model":         (llm_perf or {}).get("model", _active_model()),
            "mode":          mode,
            "elapsed_ms":    elapsed_ms,
            "prompt_tokens": (llm_perf or {}).get("prompt_tokens", 0),
            "gen_tokens":    (llm_perf or {}).get("gen_tokens", 0),
            "tokens_per_sec":(llm_perf or {}).get("tokens_per_sec", 0),
            "edit_ok":       edit_ok,
            "compile_ok":    compile_ok,
        }
        _record_metric(entry)
        perf_out = {
            "elapsed_s":      round(elapsed_ms / 1000, 1),
            "gen_tokens":     entry["gen_tokens"],
            "tokens_per_sec": entry["tokens_per_sec"],
            "compile_ok":     compile_ok,
        }
        return _resp({**data, "perf": perf_out}, sid, is_new)

    # ══════════════════════════════════════════════════════════════════════
    # IMAGE-TO-LaTeX CREATE MODE
    # When images are attached the model generates a complete new document
    # from scratch — no SEARCH/REPLACE, full document output.
    # Note: image presence takes precedence over the mode field.  The frontend
    # always sends mode=="image" when images are attached, but guarding on
    # the image list itself makes the dispatch unambiguous regardless.
    # ══════════════════════════════════════════════════════════════════════
    if images:
        try:
            raw, llm_perf = await _llm(_CREATE_SYS, messages, images=images)
        except Exception:
            return _resp(
                {"message": "", "updated": False,
                 "error": ("Can't reach Ollama. Make sure it's running and the active model "
                           "supports vision (llava, minicpm-v, moondream). "
                           "Try: ollama run llava")},
                sid, is_new, status=500,
            )

        latex = _extract_latex(_strip_fences(raw))

        # Require BOTH markers — missing either means the document won't compile.
        if "\\documentclass" not in latex or "\\begin{document}" not in latex:
            return _finish(
                {"message": ("The model didn't return complete LaTeX. "
                             "Make sure you're using a vision-capable model "
                             "(llava, minicpm-v, moondream) and try again.\n\n"
                             + raw[:500]),
                 "updated": False, "error": None},
                edit_ok=False, compile_ok=None,
            )

        tex.write_text(latex, encoding="utf-8")
        c = await _compile(sid)

        if c["ok"]:
            shutil.copy2(tex, _last(sid))
            return _finish(
                {"message": "Document recreated from image and compiled successfully.", "updated": True, "error": None},
                edit_ok=True, compile_ok=True,
            )
        # Try one auto-fix pass on compile failure
        broken     = tex.read_text(encoding="utf-8")
        fix_system = _FIX_SYS.format(
            document=broken.replace("{", "{{").replace("}", "}}"),
            error=c["error"].replace("{", "{{").replace("}", "}}"),
        )
        try:
            fr, _ = await _llm(fix_system,
                               [{"role": "user", "content": "Fix the compile error."}])
            fp = _parse(fr)
            if fp["edits"]:
                fa = _apply(broken, fp["edits"])
                if fa["ok"]:
                    tex.write_text(fa["content"], encoding="utf-8")
                    c = await _compile(sid)
                    if c["ok"]:
                        shutil.copy2(tex, _last(sid))
                        return _finish(
                            {"message": "Document recreated from image (with auto-fix) and compiled.", "updated": True, "error": None},
                            edit_ok=True, compile_ok=True,
                        )
        except Exception:
            pass
        await _revert(sid)
        return _finish(
            {"message": (f"Generated LaTeX from image but it wouldn't compile. "
                         f"Try a different vision model or edit the source manually.\n\n{c['error']}"),
             "updated": False, "error": None},
            edit_ok=True, compile_ok=False,
        )

    # ══════════════════════════════════════════════════════════════════════
    # MATH SOLVER MODE
    # Generates a complete step-by-step LaTeX solution document from scratch
    # (or refines the existing one on follow-up messages).
    # ══════════════════════════════════════════════════════════════════════
    if mode == "math":
        # Bootstrap: when there's no document yet, seed the context with the
        # math template so the model has a complete compilable skeleton to
        # reference.  This prevents "Missing \begin{document}" errors that
        # occur when the model anchors on an empty context and omits the
        # LaTeX preamble.
        math_doc = document.strip()
        if not math_doc:
            tmpl_path = TEMPLATES_DIR / "math.tex"
            if tmpl_path.exists():
                math_doc = tmpl_path.read_text(encoding="utf-8")

        # Escape { and } in the document so Python's str.format() doesn't
        # mistake LaTeX braces (e.g. \frac{a}{b}) for format placeholders.
        math_system = _MATH_SYS.format(
            document=math_doc.replace("{", "{{").replace("}", "}}")
        )
        try:
            raw, llm_perf = await _llm(math_system, messages)
        except Exception:
            model = _active_model()
            return _resp(
                {"message": "", "updated": False,
                 "error": f"Can't reach Ollama. Is it running? Try: ollama run {model}"},
                sid, is_new, status=500,
            )

        latex = _extract_latex(_strip_fences(raw))

        # Require BOTH markers — a document missing either will fail to compile.
        # Previously used `and` so a response with \documentclass but no
        # \begin{document} would silently pass and produce a pdflatex error.
        if "\\documentclass" not in latex or "\\begin{document}" not in latex:
            return _finish(
                {"message": ("The model returned an incomplete LaTeX document. "
                             "Try rephrasing the problem or switching to a "
                             "different model.\n\n" + latex[:400]),
                 "updated": False, "error": None},
                edit_ok=False, compile_ok=None,
            )

        tex.write_text(latex, encoding="utf-8")
        c = await _compile(sid)

        if c["ok"]:
            shutil.copy2(tex, _last(sid))
            return _finish(
                {"message": "Solution compiled.", "updated": True, "error": None},
                edit_ok=True, compile_ok=True,
            )

        # One auto-fix attempt on compile failure
        broken     = tex.read_text(encoding="utf-8")
        fix_system = _FIX_SYS.format(
            document=broken.replace("{", "{{").replace("}", "}}"),
            error=c["error"].replace("{", "{{").replace("}", "}}"),
        )
        try:
            fr, _ = await _llm(fix_system,
                               [{"role": "user", "content": "Fix the compile error."}])
            fp = _parse(fr)
            if fp["edits"]:
                fa = _apply(broken, fp["edits"])
                if fa["ok"]:
                    tex.write_text(fa["content"], encoding="utf-8")
                    c = await _compile(sid)
                    if c["ok"]:
                        shutil.copy2(tex, _last(sid))
                        return _finish(
                            {"message": "Solution compiled (with auto-fix).", "updated": True, "error": None},
                            edit_ok=True, compile_ok=True,
                        )
        except Exception:
            pass

        await _revert(sid)
        return _finish(
            {"message": f"Generated solution but it wouldn't compile. "
                        f"Try rephrasing the problem or switching to a different model.\n\n{c['error']}",
             "updated": False, "error": None},
            edit_ok=True, compile_ok=False,
        )

    # ══════════════════════════════════════════════════════════════════════
    # STANDARD EDIT MODE  (SEARCH/REPLACE)
    # ══════════════════════════════════════════════════════════════════════
    # Escape LaTeX braces before format() so \frac{a}{b} doesn't cause KeyError.
    system = _CHAT_SYS.format(document=document.replace("{", "{{").replace("}", "}}"))

    try:
        reply, llm_perf = await _llm(system, messages)
    except Exception:
        model = _active_model()
        return _resp(
            {"message": "", "updated": False,
             "error": f"Can't reach Ollama. Is it running? Try: ollama run {model}"},
            sid, is_new, status=500,
        )

    parsed = _parse(reply)

    if not parsed["edits"]:
        return _finish(
            {"message": parsed["message"], "updated": False, "error": None},
            edit_ok=False, compile_ok=None,
        )

    # Apply edits; one corrective retry when SEARCH text isn't found
    applied = _apply(document, parsed["edits"])
    if not applied["ok"]:
        try:
            retry = messages + [
                {"role": "assistant", "content": reply},
                {"role": "user",
                 "content": "Your SEARCH text was not found verbatim in the document. "
                            "Open document.tex above and copy the lines "
                            "character-for-character — every space, backslash, and brace "
                            "must match exactly. Include 2-3 surrounding lines for context. "
                            "Do NOT paraphrase or change any whitespace."},
            ]
            r2, _ = await _llm(system, retry)
            p2 = _parse(r2)
            if p2["edits"]:
                a2 = _apply(document, p2["edits"])
                if a2["ok"]:
                    applied, parsed = a2, p2
                else:
                    # Retry still couldn't locate the text; surface its message
                    # (not the first attempt's) in the failure response below.
                    parsed = p2
        except Exception:
            pass

    if not applied["ok"]:
        return _finish(
            {"message": f"{parsed['message']}\n\n"
                        "[!] Couldn't locate the text to change. Try rephrasing.",
             "updated": False, "error": None},
            edit_ok=False, compile_ok=None,
        )

    # Write + compile; one AI repair attempt on compile failure
    tex.write_text(applied["content"], encoding="utf-8")
    c = await _compile(sid)

    if not c["ok"]:
        broken     = tex.read_text(encoding="utf-8")
        fix_system = _FIX_SYS.format(
            document=broken.replace("{", "{{").replace("}", "}}"),
            error=c["error"].replace("{", "{{").replace("}", "}}"),
        )
        try:
            fr, _ = await _llm(fix_system,
                               [{"role": "user", "content": "Fix the compile error."}])
            fp = _parse(fr)
            if fp["edits"]:
                fa = _apply(broken, fp["edits"])
                if fa["ok"]:
                    tex.write_text(fa["content"], encoding="utf-8")
                    c = await _compile(sid)
        except Exception:
            pass

    if not c["ok"]:
        await _revert(sid)
        msg = (f"{parsed['message']}\n\n"
               f"[!] That change wouldn't compile, so I kept the previous version.\n"
               f"{c['error']}")
        return _finish({"message": msg, "updated": False, "error": None},
                       edit_ok=True, compile_ok=False)

    return _finish({"message": parsed["message"], "updated": True, "error": None},
                   edit_ok=True, compile_ok=True)
