_EDIT_RX = re.compile(
    r"<{5,}\s*SEARCH[^\n]*\n(.*?)\n={5,}[^\n]*\n(.*?)\n>{5,}\s*REPLACE",
    re.DOTALL | re.IGNORECASE,   # accept lowercase search/replace from some models
)


def _parse(text: str) -> dict:
    # Build match list once; reuse for both edits and leading-text extraction.
    matches = list(_EDIT_RX.finditer(text))
    # Strip leading/trailing blank lines from each captured group.
    # Models (especially Phi-4) sometimes add an extra blank line inside the
    # marker fences; stripping it here prevents false "not found" failures.
    edits   = [{"search": m.group(1).strip("\n"),
                 "replace": m.group(2).strip("\n")} for m in matches]
    msg     = text[: matches[0].start()].strip() if matches else text.strip()
    return {"message": msg or "Done.", "edits": edits}


def _apply(content: str, edits: list) -> dict:
    c = content.replace("\r", "")
    for e in edits:
        s = e["search"].replace("\r", "")
        r = e["replace"].replace("\r", "")
        # Reject empty OR whitespace-only SEARCH text. A blank "   " is truthy
        # but would match the first run of spaces in the document and corrupt it.
        if not s.strip():
            return {"ok": False, "content": content, "error": "Empty SEARCH block."}

        # ── Primary: exact match ──────────────────────────────────────────
        if s in c:
            c = c.replace(s, r, 1)
            continue

        # ── Fallback: trailing-whitespace-normalised match ───────────────
        # Models often copy the snippet correctly but strip trailing spaces
        # or add a stray blank line, making exact match fail even though the
        # intent is unambiguous.  Normalise both sides (rstrip each line),
        # check for a line-boundary match, then splice at the line level so
        # the rest of the document is untouched.
        had_trailing_nl = c.endswith("\n")
        c_lines = c.splitlines()
        s_norm  = "\n".join(ln.rstrip() for ln in s.splitlines())
        c_norm  = "\n".join(ln.rstrip() for ln in c_lines)
        idx     = c_norm.find(s_norm) if s_norm else -1
        if idx != -1:
            # Accept only when the match starts AND ends on a line boundary, so
            # a short search can't splice out the middle or tail of a longer line
            # (e.g. "\section{Intro}" must not match "\section{Introduction}").
            end          = idx + len(s_norm)
            starts_clean = idx == 0 or c_norm[idx - 1] == "\n"
            ends_clean   = end == len(c_norm) or c_norm[end] == "\n"
            if starts_clean and ends_clean:
                line_start = c_norm[:idx].count("\n")
                line_count = s_norm.count("\n") + 1
                c = "\n".join(
                    c_lines[:line_start]
                    + r.splitlines()
                    + c_lines[line_start + line_count:]
                )
                # Restore the trailing newline this working copy had before the
                # splice (splitlines/join drops it). Anchor on `c`, not the
                # original `content`, so earlier edits' changes are respected.
                if had_trailing_nl and not c.endswith("\n"):
                    c += "\n"
                continue

        return {"ok": False, "content": content,
                "error": f"SEARCH text not found:\n{s}"}
    return {"ok": True, "content": c, "error": None}


# ---------------------------------------------------------------------------
# LLM client -- Ollama now; set USE_VLLM=true + LLM_BASE_URL to switch
# Returns (content, perf_dict | None).  perf_dict contains Ollama's native
# timing and token stats; it is None when using vLLM (not exposed there).
# ---------------------------------------------------------------------------
async def _llm(system: str, messages: list,
               images: list[str] | None = None) -> tuple[str, dict | None]:
    """Call the active model.  images is a list of base64-encoded image strings
    (for vision models); they are attached to the last user message."""
    model = _active_model()
    msgs  = [{"role": "system", "content": system}] + messages

    # Attach images to the last user message when provided
    if images and msgs and msgs[-1].get("role") == "user":
        msgs[-1] = {**msgs[-1], "images": images}

    # Use a larger context window for image-to-LaTeX tasks so the model can
    # output a complete document without being cut off.
    num_ctx = 16384 if images else 8192

    # FIX #10: separate connect vs. read timeout.
    async with httpx.AsyncClient(timeout=_LLM_TIMEOUT) as client:
        if USE_VLLM:
            # vLLM exposes the OpenAI-compatible endpoint
            r = await client.post(f"{LLM_BASE_URL}/v1/chat/completions", json={
                "model": model, "messages": msgs,
                "temperature": 0.1, "stream": False,
            })
        else:
            # Ollama native endpoint (supports num_ctx for context window)
            r = await client.post(f"{OLLAMA_URL}/api/chat", json={
                "model": model, "messages": msgs, "stream": False,
                "options": {"temperature": 0.1, "num_ctx": num_ctx},
            })
        r.raise_for_status()
        data = r.json()
        content = (data["choices"][0]["message"]["content"] if USE_VLLM
                   else data["message"]["content"])

        # Extract Ollama's native performance counters from the response body.
        # These are only present on the Ollama path (stream:false returns them
        # in the single response object).  vLLM does not expose them.
        perf: dict | None = None
        if not USE_VLLM:
            eval_dur_ns  = data.get("eval_duration") or 0
            total_dur_ns = data.get("total_duration") or 0
            gen_tok      = data.get("eval_count") or 0
            perf = {
                "model":         data.get("model", model),
                "prompt_tokens": data.get("prompt_eval_count") or 0,
                "gen_tokens":    gen_tok,
                "total_ms":      round(total_dur_ns / 1_000_000),
                "tokens_per_sec": (round(gen_tok / (eval_dur_ns / 1e9), 1)
                                   if eval_dur_ns > 0 else 0),
            }
        return content, perf


