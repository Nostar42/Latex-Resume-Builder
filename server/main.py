# FIX (High): validate model names before writing to disk.
# Accepts only the characters Ollama uses in its own naming scheme.
_MODEL_NAME_RX = re.compile(r'^[a-zA-Z0-9:._\-]{1,200}$')

# FIX (High): block LaTeX \input / \include pointing to absolute paths or
# path-traversal sequences. -no-shell-escape already blocks \write18 (RCE);
# this closes the remaining file-disclosure vector.
_GUARD_LATEX_RX = re.compile(
    r'\\(?:input|include|openin)\b\s*(?:\d+\s*=\s*)?\{([^}]*)\}',
    re.IGNORECASE,
)
# Matches the no-brace form used by plain TeX: \input filename
# (space-separated, no curly braces).  Standard pdflatex supports this too.
_GUARD_LATEX_RX2 = re.compile(
    r'\\input\b\s+([^\s{%\\][^\s{%]*)',
    re.IGNORECASE,
)

_PATH_SUSPICIOUS = re.compile(r'^[A-Za-z]:[/\\]')

def _path_is_external(path: str) -> bool:
    return ('..' in path
            or path.startswith('/')
            or path.startswith('\\')
            or bool(_PATH_SUSPICIOUS.match(path)))

def _guard_latex(latex: str) -> str | None:
    """Return an error string if the source contains suspicious file-access
    directives, or None if safe to compile."""
    for m in _GUARD_LATEX_RX.finditer(latex):
        path = m.group(1).strip()
        if _path_is_external(path):
            return (f"Blocked: LaTeX directive references an external path "
                    f"('{path}'). Only relative includes within the document "
                    f"directory are allowed.")
    # Also check the no-brace plain-TeX form: \input filename
    for m in _GUARD_LATEX_RX2.finditer(latex):
        path = m.group(1).strip()
        if _path_is_external(path):
            return (f"Blocked: \\input references an external path "
                    f"('{path}'). Only relative includes are allowed.")
    return None


# Strips ```latex ... ``` or ``` ... ``` fences that vision models sometimes add.
_FENCE_RX = re.compile(r"^```[a-zA-Z]*\n(.*?)\n```\s*$", re.DOTALL)

def _strip_fences(text: str) -> str:
    m = _FENCE_RX.match(text.strip())
    return m.group(1).strip() if m else text.strip()


def _extract_latex(text: str) -> str:
    """Extract just the LaTeX document from \documentclass to \end{document}.

    Models frequently prefix their output with prose ('Certainly! Here is...')
    or suffix it with commentary.  Slicing to the known document boundaries
    prevents pdflatex seeing 'C' as line 1 and failing with
    'Missing \\begin{document}'.
    """
    start = text.find("\\documentclass")
    end   = text.rfind("\\end{document}")
    if start != -1 and end != -1:
        return text[start : end + len("\\end{document}")].strip()
    return text.strip()


